// Atividade Controle de fluxo
console.log("Meu primeiro if.")
let status = true
//Criando uma variável chamada "status" e atribuindo uma booleano.
if(status==true){
//Contruindo uma condicional if/else. Se o conteúdo da variavel "status" for true, será executado essa parte.
    console.log("O valor é true - verdadeiro.")    
}else{
//Se a primeira condição não for verdadeira, sera executado essa parte.
    console.log("O valor é false - falso.")
}

